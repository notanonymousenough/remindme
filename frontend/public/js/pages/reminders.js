function add_teg()
{
    let element = document.getElementById('add-teg');
    element.style.display = 'block';
    let btn_teg = document.getElementById('btn-add');
    btn_teg.style.display = 'none';

}
function save_teg()
{
    let element = document.getElementById('add-teg');
    element.style.display = 'none';
    let btn_teg = document.getElementById('btn-add');
    btn_teg.style.display = 'block';

}
function cancel_teg()
{
    let element = document.getElementById('add-teg');
    element.style.display = 'none';
    let btn_teg = document.getElementById('btn-add');
    btn_teg.style.display = 'block';
}
